package day1;

public class AreaPerimeterOfCircle {

	public static void main(String[] args) {
		double r = 7.5;// r for Radius
		double d = r * 2;// d for Diameter
		double p = Math.PI * d;// p for parameter
		double a = Math.PI * (r * r);// a for Area
		System.out.println("Parameter is " + p);
		System.out.println("Area is " + a );
		

	}

}
