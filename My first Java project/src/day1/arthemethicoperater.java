package day1;

public class arthemethicoperater {

	public static void main(String[] args) {
		int num1 = 125;
		int num2 = 24;
		
		int sum = num1 + num2;
		System.out.println("the sum of " + num1 + " + " + num2 + " = " + sum);
		int diff = num1 - num2;
		System.out.println("the difference of " + diff);
		int mult = num1 * num2;
		System.out.println("the multiplication of " + num1 + " * " + num2 + " = " + mult);
		int div = num1 / num2;
		System.out.println("the division of " +  div);
		int mod = num1 % num2;
		System.out.println("the module of " + mod);
		 }

}